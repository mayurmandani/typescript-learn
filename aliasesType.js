/* TypeScript Type Aliases
Type Aliases allow defining types with a custom name (an Alias).
Type Aliases can be used for primitives like string or more complex types such as objects and arrays
*/
var carYear = 2001;
var carType = "Toyoto";
var carModel = "BMW";
var cars = {
    year: carYear,
    type: carType,
    model: carModel
};
console.log(cars);
